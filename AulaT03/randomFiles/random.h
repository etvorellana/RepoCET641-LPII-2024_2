/*
    * Função que gera um número inteiro aleatório entre min e max
    * Parâmetros:
    *  min: valor mínimo que o número gerado pode ter
    *  max: valor máximo que o número gerado pode ter
    * Retorno:
    *  int: número gerado
    *
*/
int geraInt(int min, int max);


/*
    * Função que gera um número real aleatório entre 0 e 1
    * Parâmetros:
    *  void
    * Retorno:
    *  double: número gerado
    *
*/
double geraRand();

/*
    * Função que gera um número real aleatório entre min e max
    * Parâmetros:
    *  min: valor mínimo que o número gerado pode ter
    *  max: valor máximo que o número gerado pode ter
    * Retorno:
    *  float: número gerado
    *
*/

double geraReal(double min, double max);

